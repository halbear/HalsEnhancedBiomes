@HebModElements.ModElement.Tag
public class FallingleavesBlock extends HebModElements.ModElement {
	@ObjectHolder("heb:fallingleaves")
	public static final Block block = null;

	public FallingleavesBlock(HebModElements instance) {
		super(instance, 15);
	}

	@Override
	public void initElements() {
		elements.blocks.add(() -> new CustomBlock());
		elements.items.add(() -> new BlockItem(block, new Item.Properties().group(null)).setRegistryName(block.getRegistryName()));
	}

	@Override
	@OnlyIn(Dist.CLIENT)
	public void clientLoad(FMLClientSetupEvent event) {
		RenderTypeLookup.setRenderLayer(block, RenderType.getCutout());
	}

	public static class CustomBlock extends Block {
		public CustomBlock() {
			super(Block.Properties.create(Material.AIR).sound(SoundType.GLASS).hardnessAndResistance(-1, 3600000).setLightLevel(s -> 1)
					.doesNotBlockMovement().notSolid().setNeedsPostProcessing((bs, br, bp) -> true).setEmmisiveRendering((bs, br, bp) -> true)
					.setOpaque((bs, br, bp) -> false));
			setRegistryName("fallingleaves");
		}

		@Override
		public int getOpacity(BlockState state, IBlockReader worldIn, BlockPos pos) {
			return 15;
		}

		@Override
		public VoxelShape getShape(BlockState state, IBlockReader world, BlockPos pos, ISelectionContext context) {
			return VoxelShapes.empty();
		}

		@Override
		public boolean isReplaceable(BlockState state, BlockItemUseContext context) {
			return context.getItem().getItem() != this.asItem();
		}

		@Override
		public List<ItemStack> getDrops(BlockState state, LootContext.Builder builder) {
			List<ItemStack> dropsOriginal = super.getDrops(state, builder);
			if (!dropsOriginal.isEmpty())
				return dropsOriginal;
			return Collections.singletonList(new ItemStack(this, 1));
		}

		@OnlyIn(Dist.CLIENT)
		@Override
		public void animateTick(BlockState blockstate, World world, BlockPos pos, Random random) {
			super.animateTick(blockstate, world, pos, random);
			PlayerEntity entity = Minecraft.getInstance().player;
			int x = pos.getX();
			int y = pos.getY();
			int z = pos.getZ();
			if (true)
				for (int l = 0; l < 2; ++l) {
					double d0 = (x + 0.5) + (random.nextFloat() - 0.5) * 0.8000000014901161D * 20;
					double d1 = ((y + 0.7) + (random.nextFloat() - 0.5) * 0.8000000014901161D) + 0.5;
					double d2 = (z + 0.5) + (random.nextFloat() - 0.5) * 0.8000000014901161D * 20;
					world.addParticle(FallingleafParticle.particle, d0, d1, d2, 0, 0, 0);
				}
		}
	}
}
